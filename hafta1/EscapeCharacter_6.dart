void main() {
    // \n -> Yeni satıra geçer
    print("Merhaba\nDart");

    // \t -> Tab boşluğu bırakır
    print("Ad\tSoyad");

    // \" -> Çift tırnak yazmak için
    print("Dart dilinde \"string\" kullanımı");

    // \' -> Tek tırnak yazmak için
    print('O\'Brien ailesi');

    // \\ -> Ters eğik çizgi (\) göstermek için
    print("C:\\Users\\Ahmet\\Desktop");

    // \r -> Satır başına döner (sonraki yazı baştan yazar, bazı konsollarda çalışır)
    print("Merhaba Dünya\rSelam");
}
